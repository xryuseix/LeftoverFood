/** @format */

import { zip } from "./utils";

test("utils.zip", () => {
  expect(zip<number>([1, 2, 3], [4, 5, 6])).toEqual([
    [1, 4],
    [2, 5],
    [3, 6],
  ]);
  expect(zip(["1", "2", "3"], ["4", "5", "6"])).toEqual([
    ["1", "4"],
    ["2", "5"],
    ["3", "6"],
  ]);
  expect(zip([["1"], ["2"], ["3"]], [["4"], ["5"], ["6"]])).toEqual([
    [["1"], ["4"]],
    [["2"], ["5"]],
    [["3"], ["6"]],
  ]);
  expect(zip([1, 2, 3], [4, 5])).toEqual([
    [1, 4],
    [2, 5],
  ]);
});
